package org.gwm.splice.client.service.query;

/**
 * A query expression that simply provides three Strings: column, operator and value.
 * 
 * @author andy
 *
 */
public class SimpleExpression implements IQueryExpression {
	
	String columnName;
	String operator;
	String value;
	
	public SimpleExpression(String columnName, String operator, String value) {
		this.columnName = columnName;
		this.operator = operator;
		this.value = value;
	}
	
	///////////////////////////////////////////////////////////////////////////////////
	
	public String getColumnName() {
		return columnName;
	}
	public void setColumnName(String columnName) {
		this.columnName = columnName;
	}
	public String getOperator() {
		return operator;
	}
	public void setOperator(String operator) {
		this.operator = operator;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}

}
